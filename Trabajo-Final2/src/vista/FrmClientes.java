    public void limpiar() {
        txtNombre.setText(null);
        txtApellido.setText(null);
        txtId.setText(null);
        txtTelefono.setText(null);
        txtDireccion.setText(null);
        pos = -1;
        editarCliente = false;
    }

    public void cargarTabla(ListaEnlazadaServices<Cliente> lec) {
        TablaClientes tc = new TablaClientes(lec);
        tblCliente.setModel(tc);
        tblCliente.updateUI();
    }

    public void guardar() throws Exception {
        if (txtNombre.getText().trim().isEmpty() || txtApellido.getText().trim().isEmpty() || txtId.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos ", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                if (editarCliente) {
                    cc.getListaCliente().obtenerDato(pos).setNombres(txtNombre.getText() + " " + txtApellido.getText());
                    cc.getListaCliente().obtenerDato(pos).setIdentificacion(Integer.valueOf(txtId.getText()));
                    cc.getListaCliente().obtenerDato(pos).setDireccion(txtDireccion.getText());
                    cc.getListaCliente().obtenerDato(pos).setTelefono(txtTelefono.getText());
                } else {
                    cc.getListaCliente().insertarAlInicio(new Cliente(txtNombre.getText() + " " + txtApellido.getText(), Integer.valueOf(txtId.getText()), txtTelefono.getText(), txtDireccion.getText()));
                }
                cc.guardar();        // TODO add your handling code here:
                cargarTabla(cc.getListaCliente());
                limpiar();
            } catch (IOException ex) {
                System.out.println("no se pudo guardar los datos");
            }
        }
    }

    public void editar() {
        int fila = tblCliente.getSelectedRow();
        if (fila >= 0) {
            String[] x = (cc.getListaCliente().obtenerDato(fila).getNombres()).split(" ");
            txtNombre.setText(x[0]);
            txtApellido.setText(x[1]);
            txtId.setText(String.valueOf(cc.getListaCliente().obtenerDato(fila).getIdentificacion()));
            txtTelefono.setText(String.valueOf(cc.getListaCliente().obtenerDato(fila).getTelefono()));
            txtDireccion.setText(String.valueOf(cc.getListaCliente().obtenerDato(fila).getDireccion()));
            pos = fila;
            editarCliente = true;
        } else {
            JOptionPane.showMessageDialog(null, "Elige algun registro de la tabla", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void eliminar() {
        int fila = tblCliente.getSelectedRow();
        if (fila >= 0) {
            cc.getListaCliente().eliminarPosicion(fila);
            try {
                cc.guardar();
            } catch (IOException ex) {
                Logger.getLogger(FrmClientes.class.getName()).log(Level.SEVERE, null, ex);
            }
            cargarTabla(cc.getListaCliente());
        } else {
            JOptionPane.showMessageDialog(null, "Elige algun registro de la tabla", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void buscar() throws Exception {
        if (!txtBuscar.getText().trim().isEmpty()) {
            boolean isNumeric = (txtBuscar.getText()).matches("[+-]?\\d*(\\.\\d+)?");
            if (isNumeric) {
                busqueda.setLista(cc.getListaCliente().getLista().buscar_Secuencial("identificacion", Integer.parseInt(txtBuscar.getText())));
            } else {
                busqueda.setLista(cc.getListaCliente().getLista().buscar_Secuencial("nombres", txtBuscar.getText()));
            }
            if ((busqueda.getSize() > 0)) {
                cargarTabla(busqueda);
            }
        } else {
            busqueda = new ListaEnlazadaServices<>();
            cargarTabla(cc.getListaCliente());
        }
    }

